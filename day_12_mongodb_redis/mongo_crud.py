# 2. READ
# ─────────────────────────────────────────────
def read_documents(db):
    print("\n── READ ────────────────────────────────")
    col = db["experiments"]

    # find_one - returns first matching document
    doc = col.find_one({"model_type": "BERT"})
    print(f"  find_one BERT  → {doc['name']} | f1: {doc['metrics']['test_f1']}")

    # find with projection - 1=include 0=exclude
    print("\n  All completed experiments:")
    for doc in col.find({"status": "completed"}, {"name": 1, "model_type": 1, "metrics.test_f1": 1, "_id": 0}):
        print(f"    {doc['model_type']:15s} | {doc['name']:28s} | f1: {doc['metrics'].get('test_f1','N/A')}")

    # comparison operators: $gt $lt $gte $lte $ne
    print("\n  Models with f1 > 0.92:")
    for doc in col.find({"metrics.test_f1": {"$gt": 0.92}}, {"name": 1, "metrics.test_f1": 1, "_id": 0}):
        print(f"    {doc['name']} → {doc['metrics']['test_f1']}")

    # sort + limit - like ORDER BY LIMIT in SQL
    print("\n  Top 3 by F1 score:")
    top3 = col.find({}, {"name": 1, "metrics.test_f1": 1, "_id": 0}).sort("metrics.test_f1", -1).limit(3)
    for i, doc in enumerate(top3, 1):
        print(f"    #{i} {doc['name']:28s} → {doc['metrics'].get('test_f1','N/A')}")

    # count documents
    total     = col.count_documents({})
    completed = col.count_documents({"status": "completed"})
    print(f"\n  Count → total: {total} | completed: {completed} | other: {total - completed}")


