"""
Day 12: MongoDB CRUD Operations
Author: Sheshikala
Date: 2026-03-12

What I learned:
- MongoDB stores data as documents (like JSON) not rows like SQL
- No fixed schema needed - each document can have different fields
- This is really useful for ML experiments where hyperparameters differ per model
- _id is auto-generated by MongoDB
- find() returns a cursor not a list - need list() to see all results
- dot notation (metrics.test_f1) to access nested fields in queries

How to run:
  1. Start MongoDB: docker run -d -p 27017:27017 --name mongodb mongo
  2. pip install pymongo
  3. python mongo_crud.py
"""

