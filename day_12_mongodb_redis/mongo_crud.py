# 3. UPDATE
# ─────────────────────────────────────────────
def update_documents(db):
    print("\n── UPDATE ──────────────────────────────")
    col = db["experiments"]

    # update_one with $set and $push
    # $set changes fields, $push adds item to array
    result = col.update_one(
        {"name": "XGBoost Fraud Detector"},
        {
            "$set" : {"status": "completed", "metrics.test_f1": 0.9310},
            "$push": {"tags": "Production-Ready"}
        }
    )
    print(f"  update_one     → matched: {result.matched_count} | modified: {result.modified_count}")

    # update_many - add domain field to all NLP experiments
    result = col.update_many(
        {"tags": "NLP"},
        {"$set": {"domain": "Natural Language Processing"}}
    )
    print(f"  update_many    → NLP domain field added to {result.modified_count} docs")

    # upsert - creates document if it doesn't exist
    col.update_one(
        {"name": "LSTM Time Series"},
        {"$set": {
            "model_type": "LSTM",
            "project"   : "Sales Forecasting",
            "status"    : "pending",
            "researcher": "Sheshikala",
            "created_at": datetime.datetime.now()
        }},
        upsert=True
    )
    print(f"  upsert         → LSTM created (didn't exist before)")
    print(f"  Total now      → {col.count_documents({})}")


