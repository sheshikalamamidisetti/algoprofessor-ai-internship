# 4. QUERY OPERATORS
# ─────────────────────────────────────────────
def query_operators(db):
    print("\n── QUERY OPERATORS ─────────────────────")
    col = db["experiments"]

    # $in - match any value in a list
    print("  $in  - BERT or RoBERTa models:")
    for doc in col.find({"model_type": {"$in": ["BERT", "RoBERTa"]}}, {"name": 1, "_id": 0}):
        print(f"    {doc['name']}")

    # $exists - check if a field exists
    print("\n  $exists - experiments with rmse metric:")
    for doc in col.find({"metrics.rmse": {"$exists": True}}, {"name": 1, "metrics.rmse": 1, "_id": 0}):
        print(f"    {doc['name']} → rmse: {doc['metrics']['rmse']}")

    # $and - multiple conditions must all be true
    print("\n  $and - completed NLP experiments:")
    for doc in col.find({"$and": [{"status": "completed"}, {"tags": "NLP"}]}, {"name": 1, "_id": 0}):
        print(f"    {doc['name']}")

    # $regex - pattern matching (like LIKE in SQL)
    print("\n  $regex - name contains 'Sentiment':")
    for doc in col.find({"name": {"$regex": "Sentiment", "$options": "i"}}, {"name": 1, "_id": 0}):
        print(f"    {doc['name']}")

    # $elemMatch - match element inside array
    print("\n  $elemMatch - has 'Transformer' tag:")
    for doc in col.find({"tags": {"$elemMatch": {"$eq": "Transformer"}}}, {"name": 1, "tags": 1, "_id": 0}):
        print(f"    {doc['name']} → {doc['tags']}")


# ─────────────────────────────────────────────
# 5. INDEXES
# ─────────────────────────────────────────────
def create_indexes(db):
    print("\n── INDEXES ─────────────────────────────")
    col = db["experiments"]

    # single field indexes - makes queries on these fields faster
    col.create_index("model_type")
    col.create_index("status")
    col.create_index("researcher")

    # compound index - for queries filtering by project AND status together
    col.create_index([("project", 1), ("status", 1)])

    # text index - for full text search on name field
    col.create_index([("name", "text")])

    print("  Created indexes: model_type, status, researcher, project+status, text(name)")
    print("  All indexes on collection:")
    for idx in col.list_indexes():
        print(f"    {idx['name']}")


# ─────────────────────────────────────────────
# 6. DELETE
# ─────────────────────────────────────────────
def delete_documents(db):
    print("\n── DELETE ──────────────────────────────")
    col = db["experiments"]

    # delete_one
    result = col.delete_one({"name": "LSTM Time Series"})
    print(f"  delete_one     → deleted: {result.deleted_count} (LSTM)")

    # delete_many - careful, deletes all matching!
    result = col.delete_many({"status": "pending"})
    print(f"  delete_many    → deleted: {result.deleted_count} pending experiments")

    print(f"  Remaining      → {col.count_documents({})} documents")


